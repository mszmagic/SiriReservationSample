import UIKit
import SwiftUI

class SceneDelegate: UIResponder, UIWindowSceneDelegate {

    var window: UIWindow?


    func scene(_ scene: UIScene, willConnectTo session: UISceneSession, options connectionOptions: UIScene.ConnectionOptions) {
        let contentView = ContentView()

        if let windowScene = scene as? UIWindowScene {
            let window = UIWindow(windowScene: windowScene)
            window.rootViewController = UIHostingController(rootView: contentView)
            self.window = window
            window.makeKeyAndVisible()
        }
        
    }
    
    /*
     ユーザーが予約を表示するためにクリックしたときに実行するアクションを処理します。
     */
    func scene(_ scene: UIScene, continue userActivity: NSUserActivity) {
        if userActivity.activityType == "INGetReservationDetailsIntent" {
            /* メインビューがアクティビティオブジェクトを受信し、予約の詳細を表示できるように、アプリケーション通知を送信します。 */
            NotificationCenter.default.post(name: NotificationTypes.viewReservationDetails, object: userActivity)
        }
    }

